const buttons = document.querySelectorAll('button')

buttons.forEach(item => {
    item.onclick = () => {
        document.body.style.backgroundColor = item.innerText
    }
})

// Получает все элементы <button> на странице и сохраняет их
// в переменную buttons с помощью document.querySelectorAll('button').
// Затем используется метод forEach, чтобы выполнить указанную функцию для каждого элемента в массиве buttons.
// Внутри функции присваивается обработчик события onclick для каждой кнопки.
// Когда происходит клик по кнопке, вызывается функция обработчика.
// Эта функция изменяет цвет фона всего документа (document.body.style.backgroundColor)
// на цвет текста кнопки (item.innerText), который был нажат.
// Для того чтобы объяснить этот же код, но для JavaScript файла,
// предположим, что этот код находится в файле с расширением .js,
// который подключен к HTML-странице с помощью тега <script>.

// Такой файл содержит JavaScript-код, который выполняется при загрузке страницы.
// Когда браузер встречает этот код, он начинает выполнять его по мере возможности.

// Код выполняет те же самые действия, что и в предыдущем объяснении,
// но на этот раз он применяется к HTML-элементам после того, как страница загрузится.
// В этом случае, никаких напрямую видимых изменений на странице не происходит при выполнении кода,
// пока пользователь не взаимодействует с элементами, которые он модифицирует.